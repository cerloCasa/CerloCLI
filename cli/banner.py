_ROWS = 6


def banner(text: str) -> str:
    """Ritorna `text` (maiuscolo) renderizzato come ASCII art ANSI Shadow."""
    text = text.upper()
    rows = ['' for _ in range(_ROWS)]
    for ch in text:
        glyph = _GLYPHS.get(ch, _GLYPHS[' '])
        for i in range(_ROWS):
            rows[i] += glyph[i]
    return '\n'.join(rows)

